#include "includes.hpp"

// класс для создания игрока 
class Player
{
// приватно
private:
	std::string Name;
	int MaxHealth;
	int Health;
	int Damage;
	bool is_died = false;
// публично
public:
	// конструктор
	Player(int _health, std::string _name) {
		MaxHealth = _health;
		Name = _name;
		Damage = 1;
		Health = _health;
	}
	// получить значение здоровья игрока
	int GetHealth()
	{
		std::cout << "Your health : " << Health << '\n' << "Your maximal health : " << MaxHealth << '\n';
		return Health;
	}
	// получить значение урона игрока
	int GetDamage()
	{
		std::cout << "Your damage : " << Damage << '\n';
		return Damage;
	}
	// получить имя игрока
	std::string GetName()
	{
		std::cout << "Your name : " << Name << '\n';
		return Name;
	}
	// восстановление здоровья 
	int Healing(int value)
	{
		Health += value;
		if (Health > MaxHealth)
		{
			Health = MaxHealth;
			std::cout << "You have maximal hits\n";
		}
		std::cout << "You healing on " << value << "hits\n";
		return Health;
	}
	// вражеский урон
	void EnemyDamage(int _enemyDamage)
	{
		Health -= _enemyDamage;
		CheckDied();
		DiedMessage();
	}
	// вывести статистики игрока 
	void GetStat()
	{
		std::cout << "You have : \nName : " << Name << "\nHealth - " << Health << '/' << MaxHealth << "\nDamage - " << Damage << '\n';
	}
	// задать значение максимального здоровья
	void SetMaxHealth(int _mhealth)
	{
		MaxHealth = _mhealth;
		if (Health > MaxHealth)
		{
			Health = MaxHealth;
		}
		CheckDied();
		DiedMessage();
	}
	// проверить умер ли игрок
	bool CheckDied()
	{
		if (Health > 0)
		{
			return is_died;
		}
		is_died = true;
		return is_died;
	}
	// сообщение о смерти
	void DiedMessage()
	{
		if (is_died)
		{
			std::cout << "You died\n";
		}
	}
};